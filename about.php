<h3>What does Natame do?</h3>
<p>Natame scans the public Twitter feed looking for sick people. Data is then aggregated and analyzed looking for trending contagious diseases across the country.</p>
<h3>What diseases are being tracked?</h3>
<p>Here's the list: Anthrax, Cholera, <a href="http://www.natame.com/?DIS_ID=3">Common Cold</a>, Dengue, Diphtheria, Gastroenteritis, <a href="http://www.natame.com/?DIS_ID=7">Influenza</a>, Legionnaires' Disease, Malaria, Measles, Meningitis, Mosquito borne disease, Mumps, Pertussis, Pneumonia, Polio, Rabies, Smallpox, STD, Tetanus, Tick borne disease, Tuberculosis and Typhoid.</p>
<h3>What's up with this Nature Tracker?</h3>
<p>Yeap, this site also tracks tweets about wild nature. Some of them you may hear of in the news (blizzards, tornadoes, hurricanes, etc.); whereas others you will not (minor earthquakes &amp; UFOs) </p>
<h3>Why was this website created?</h3>
<p>This website was originally inspired by the <a href="http://www.nowtrendingchallenge.com/">#nowtrending challenge</a>. Thereafter, it just became a very cool project!</p>
